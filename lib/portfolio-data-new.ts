// Helper function to extract year from filename
function extractYear(filename: string): number {
  const yearMatch = filename.match(/\b(19\d{2}|20\d{2})\b/);
  if (yearMatch) return parseInt(yearMatch[0]);
  
  // Default years based on folder
  if (filename.includes('2025')) return 2025;
  if (filename.includes('2022') || filename.includes('2023') || filename.includes('2024')) return 2023;
  if (filename.includes('Oil on canvas')) return 1998; // Early work default
  return 2020; // Default for acrylic
}

// Helper function to extract medium from filename
function extractMedium(filename: string): string {
  if (filename.toLowerCase().includes('oil on canvas')) return 'Oil on canvas';
  if (filename.toLowerCase().includes('acrylic on canvas')) return 'Acrylic on canvas';
  if (filename.toLowerCase().includes('pigments on clay')) return 'Pigments on clay wall';
  if (filename.toLowerCase().includes('spray paint')) return 'Acrylic and spray paint on wall';
  return 'Mixed media';
}

// Helper function to clean title from filename
function cleanTitle(filename: string): string {
  return filename
    .replace(/\.(jpg|JPG|jpeg|png|PNG)$/, '')
    .replace(/\s*(oil|Oil|OIL)\s*on\s*canvas.*$/i, '')
    .replace(/\s*(acrylic|Acrylic|ACRYLIC)\s*on\s*canvas.*$/i, '')
    .replace(/\s*\d{4}\s*$/,'') // Remove year at end
    .replace(/\s+#\d+/, ' #') // Clean up series numbers
    .replace(/\s+/g, ' ')
    .trim();
}

